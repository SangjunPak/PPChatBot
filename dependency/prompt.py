from datetime import datetime
 
import pytz
date_now = datetime.now(pytz.timezone('Asia/Seoul')).strftime('%Y-%m-%d %H:%M:%S')
 
def rag_query(passages):
    context = ''
    if not passages:
        return context
      
    for passage in passages:
        context += passage['source']['content'] + '\n'
        context += '---' + '\n'
      
    return context

